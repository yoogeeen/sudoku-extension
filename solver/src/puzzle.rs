
pub fn create_puzzle() -> [[i32; 9]; 9] {
    let mut puzzle: [[i32; 9]; 9] = [[0; 9]; 9];
    puzzle[0][6] = 9;
    puzzle[0][8] = 7;
    puzzle[1][0] = 6;
    puzzle[1][2] = 2;
    puzzle[1][6] = 8;
    for &(row, col, val) in &[
        (0, 6, 9),
        (0, 8, 7),
        (1, 0, 6),
        (1, 2, 2),
        (1, 6, 8),
        (2, 7, 3),
        (3, 0, 8),
        (3, 2, 1),
        (3, 3, 4),
        (3, 4, 2),
        (4, 4, 8),
        (4, 6, 7),
        (5, 0, 7),
        (5, 4, 5),
        (5, 6, 2),
        (6, 1, 3),
        (6, 7, 7),
        (7, 1, 6),
        (7, 3, 5),
        (7, 7, 2),
        (7, 8, 1),
        (8, 2, 8),
        (8, 4, 4),
        (8, 5, 3),
    ] {
        puzzle[row][col] = val;
    }

    puzzle

}

pub fn print_puzzle(puzzle: [[i32; 9]; 9]) {
    println!("------------------------------");
    for i in 0..9 {
        for j in 0..9 {
            print!(" {} ", puzzle[i][j]);

            if (j + 1) % 3 == 0 {
                print!("|");
            }
        }
        println!();
        if (i + 1) % 3 == 0 {
            println!("------------------------------");
        }
    }
}
